CREATE TABLE student (
    studentID varchar(255),
    firstName varchar(255),
    lastName varchar(255),
    year int,
    PRIMARY KEY (studentID)
);

CREATE TABLE class (
    classID int NOT NULL AUTO_INCREMENT,
    classCode varchar(255),
    semester varchar(255),
    year int,
    professorID varchar(255),
    PRIMARY KEY (classID)
);

CREATE TABLE professor (
    professorID varchar(255), 
    firstName varchar(255), 
    lastName varchar(255), 
    instructor_rating int,
    PRIMARY KEY (professorID)
);

CREATE TABLE rating (
    rankID int NOT NULL AUTO_INCREMENT, 
    overall_rating int, 
    hours_assignment_per_week int, 
    hours_studying_per_week int, 
    num_assignments int,
    PRIMARY KEY (rankID)
);

CREATE TABLE likes (
    studentID varchar(255),
    classID varchar(255), 
    PRIMARY KEY (studentID, classID)
    #FOREIGN KEY (studentID) REFERENCES student(studentID),
    #FOREIGN KEY (classID) REFERENCES class(classID)
);
    
CREATE TABLE classCode (
    classID int,
    classCode varchar(255),
    PRIMARY KEY (classID)
    # FOREIGN KEY (classID) REFERENCES class(classID)
);

CREATE TABLE ranks (
    rankID int NOT NULL AUTO_INCREMENT,
    studentID varchar(255),
    PRIMARY KEY (rankID)
    #FOREIGN KEY (studentID) REFERENCES student(studentID),
    #FOREIGN KEY (rankID) REFERENCES rating(rankID)
);

CREATE TABLE rankAbout ( 
    rankID int NOT NULL AUTO_INCREMENT, 
    classID int,
    PRIMARY KEY (rankID)
    # FOREIGN KEY (rankID) REFERENCES rating(rankID),
    # FOREIGN KEY (classID) REFERENCES class(classID)
);

CREATE TABLE comments (
    commentID int NOT NULL AUTO_INCREMENT, 
    studentID varchar(255), 
    date_posted date, 
    content varchar(255),
    PRIMARY KEY (commentID)
    # FOREIGN KEY (studentID) REFERENCES student(studentID)    
);

CREATE TABLE login (
    username varchar(255), 
    userPassword varchar(255), 
    studentID varchar(255),
    PRIMARY KEY(username, userPassword)
    # FOREIGN KEY (studentID) REFERENCES student(studentID)    
);

CREATE TABLE studentComment (
    commentID int NOT NULL AUTO_INCREMENT, 
    studentID varchar(255),
    PRIMARY KEY (commentID)
    # FOREIGN KEY(commentID) REFERENCES comment(commentID),
    # FOREIGN KEY (studentID) REFERENCES student(studentID) 
);

CREATE TABLE classComment (
    commentID int NOT NULL AUTO_INCREMENT, 
    classID int,
    PRIMARY KEY (commentID)
    # FOREIGN KEY(commentID) REFERENCES comment(commentID),
    # FOREIGN KEY(classID) REFERENCES class(classID)
);

CREATE TABLE classYear (
    classID int, 
    year int,
    PRIMARY KEY (classID)
    # FOREIGN KEY (classID) REFERENCES class(classID)
);

CREATE TABLE classSemester (
    classID int, 
    semester varchar(255),
    PRIMARY KEY (classID)
    # FOREIGN KEY (classID) REFERENCES class(classID)
);

